// Tema 13: Arrays (Arreglos)

// Uso de arreglos para almacenar varios datos.
let colores = ["Rojo", "Azul", "Verde", "Amarillo","Rosado","Celeste"];

// Mostrar arreglo completo:
console.log(colores);

// Mostrar un elemento específico:
console.log("Primer color:", colores[2]);

// Mostrar otro elemento específico:
console.log("Segundo color:", colores[4]);