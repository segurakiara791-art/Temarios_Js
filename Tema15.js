// Tema 15: Matrices

// Uso de matrices para almacenar datos en filas y columnas.
let matriz = [

    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]

];

// Mostrar matriz completa:
console.log(matriz);

// Mostrar elementos específicos:
console.log("Elemento:", matriz[0][1]);
// Mostrar otro elemento específico:
console.log("Elemento:", matriz[2][2]);