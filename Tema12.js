// Tema 12: Case y Break

// Uso de switch para evaluar diferentes casos.

let dia = 4;

// Evaluar valor de la variable día:
switch (dia) {

    // Caso 1:
    case 1:
        console.log("Lunes");
        break;

    // Caso 2:
    case 2:
        console.log("Martes");
        break;

    // Caso 3:
    case 3:
        console.log("Miércoles");
        break;

    // Caso 4:
    case 4:
        console.log("Jueves");
        break;

    // Si no cumple ningún caso:
    default:
        // Mostrar mensaje de no valido o error:
        console.log("Día no válido");

}