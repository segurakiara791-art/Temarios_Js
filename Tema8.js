// Uso de foreach para recorrer arreglos.

let frutas = ["Manzana", "Pera", "Uva", "Fresa","Lucuma","Platano","Cereza","Naranja","Cirguela","Piña"];

// Recorrer cada fruta del arreglo:
frutas.forEach(function(fruta) {

    // Mostrar frutas en consola:
    console.log("Fruta:", fruta);

});